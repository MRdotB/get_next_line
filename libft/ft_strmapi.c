#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*r;
	unsigned int	i;

	i = 0;
	if (!(f) || !(s))
		return (NULL);
	r = (char*)ft_memalloc(sizeof(char) * ft_strlen(s) + 1);
	if (!(r))
		return (NULL);
	while (s[i])
	{
		r[i] = f(i, s[i]);
		i++;
	}
	r[i] = '\0';
	return (r);
}
